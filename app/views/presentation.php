            <p>
            Marque de bijoux fantaisie haut de gamme.

Enigmatique et déconcertant le créateur a su concevoir des bijoux issus d’un univers troublant et chic qui interpelle.

Or, argent, laiton ou chrome, il façonne ses derniers avec élégance.

Après une formation en bijouterie traditionnelle, Rémy Filiol, diplômé en DMA (diplome des métiers d' art) a côtoyé Robert Goosens et Pascal Schenal.
Au coté de ces créateurs il a acquis une maîtrise et une technicité pointue que l’on retrouve dans ses bijoux.
Fort de son savoir faire, il a crée Elessar Bijouterie.

C’est dans son atelier du centre de la France que Rémy Filiol imagine, façonne et donne vie à ses bijoux. Tout est réalisé à la main. Travaillant avec des matières premières provenant du monde, il aime utiliser des matériaux changeants, hétéroclites et ayant une âme…

Sa capacité à mélanger naturellement ces différents styles fait de lui un artiste unique.

